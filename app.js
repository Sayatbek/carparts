function t(key) {
  return I18N[state.lang][key] || key;
}

function applyTranslations() {
  document.documentElement.lang = state.lang;
  document.title = t("meta.title");
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute("content", t("meta.description"));

  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    el.setAttribute("placeholder", t(el.dataset.i18nPlaceholder));
  });

  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.lang === state.lang);
  });
}

function formatPrice(n) {
  if (n == null) return t("product.price_request");
  return new Intl.NumberFormat("ru-KZ", { style: "currency", currency: "KZT", maximumFractionDigits: 0 }).format(n);
}

function getBrandName(id) {
  const b = BRANDS.find(x => x.id === id);
  return b ? b.name : id;
}

function getCategoryName(id) {
  const c = CATEGORIES.find(x => x.id === id);
  return c ? c.name[state.lang] : id;
}

function renderBrands() {
  const grid = document.getElementById("brand-grid");
  grid.innerHTML = "";
  BRANDS.forEach(b => {
    const count = PRODUCTS.filter(p => p.brand === b.id).length;
    const chip = document.createElement("button");
    chip.className = "brand-chip" + (state.brand === b.id ? " active" : "");
    chip.innerHTML = `${b.name}<small>${count} ${count === 1 ? "позиция" : "позиций"}</small>`;
    chip.addEventListener("click", () => {
      state.brand = state.brand === b.id ? "" : b.id;
      document.getElementById("brand-filter").value = state.brand;
      renderBrands();
      renderProducts();
      document.getElementById("catalog").scrollIntoView({ behavior: "smooth", block: "start" });
    });
    grid.appendChild(chip);
  });
}

function renderFilters() {
  const brandSel = document.getElementById("brand-filter");
  brandSel.innerHTML = `<option value="">${t("filters.all_brands")}</option>` +
    BRANDS.map(b => `<option value="${b.id}">${b.name}</option>`).join("");
  brandSel.value = state.brand;

  const catSel = document.getElementById("category-filter");
  catSel.innerHTML = `<option value="">${t("filters.all_categories")}</option>` +
    CATEGORIES.map(c => `<option value="${c.id}">${c.name[state.lang]}</option>`).join("");
  catSel.value = state.category;
}

