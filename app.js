function buildProductCard(p) {
  const name = p.name[state.lang] || p.name.ru;
  const brand = getBrandName(p.brand);
  const price = formatPrice(p.price);
  const priceClass = p.price == null ? "price-on-request" : "price";
  const icon = CATEGORY_ICONS[p.category] || CATEGORY_ICONS.engine;

  const waText = encodeURIComponent(
    `Здравствуйте! Интересует: ${name} (${p.sku}). ${p.price ? "Цена: " + price : ""}`
  );
  const waUrl = `${WHATSAPP}?text=${waText}`;

  const imgSrc = p.image || CATEGORY_IMAGES[p.category];
  const fallback = `<span class="placeholder cat-${p.category}">${icon}</span>`;
  const media = imgSrc
    ? `<img src="${imgSrc}" alt="${name}" loading="lazy" data-fallback="${p.category}" />`
    : fallback;

  const card = document.createElement("article");
  card.className = "product-card";
  card.innerHTML = `
    <div class="product-media">
      ${p.badge ? `<span class="product-badge">${p.badge}</span>` : ""}
      ${media}
      ${imgSrc ? `<span class="placeholder placeholder-fallback cat-${p.category}" hidden>${icon}</span>` : ""}
    </div>
    <div class="product-body">
      <span class="product-brand">${brand}</span>
      <h3 class="product-name">${name}</h3>
      <span class="product-sku">${t("product.sku")}: ${p.sku}</span>
      <div class="product-price">
        <span class="${priceClass}">${price}</span>
      </div>
    </div>
    <div class="product-actions">
      <a class="btn btn-whatsapp btn-sm" href="${waUrl}" target="_blank" rel="noopener">${t("product.whatsapp")}</a>
      <a class="btn btn-ghost btn-sm" href="tel:${PHONE}" aria-label="${t("product.call")}">📞</a>
    </div>
  `;
  return card;
}

function getFilteredProducts() {
  const q = state.search.trim().toLowerCase();
  return PRODUCTS.filter(p => {
    if (state.brand && p.brand !== state.brand) return false;
    if (state.category && p.category !== state.category) return false;
    if (q) {
      const haystack = [
        p.sku,
        p.name.ru, p.name.kk, p.name.en,
        getBrandName(p.brand)
      ].join(" ").toLowerCase();
      if (!haystack.includes(q)) return false;
    }
    return true;
  });
}

function renderProducts() {
  const grid = document.getElementById("product-grid");
  const empty = document.getElementById("empty-state");
  const count = document.getElementById("results-count");
  const list = getFilteredProducts();

  grid.innerHTML = "";
  list.forEach(p => grid.appendChild(buildProductCard(p)));

  grid.querySelectorAll("img[data-fallback]").forEach(img => {
    img.addEventListener("error", () => {
      const fb = img.parentNode.querySelector(".placeholder-fallback");
      if (fb) fb.hidden = false;
      img.remove();
    }, { once: true });
  });

  count.textContent = t("results.count").replace("{n}", list.length);
  empty.classList.toggle("hidden", list.length > 0);
}

function bindEvents() {
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.addEventListener("click", () => {
      state.lang = btn.dataset.lang;
      localStorage.setItem("lang", state.lang);
      applyTranslations();
      renderFilters();
      renderBrands();
      renderProducts();
    });
  });

  document.getElementById("search").addEventListener("input", e => {
    state.search = e.target.value;
    renderProducts();
  });

  document.getElementById("brand-filter").addEventListener("change", e => {
    state.brand = e.target.value;
    renderBrands();
    renderProducts();
  });

  document.getElementById("category-filter").addEventListener("change", e => {
    state.category = e.target.value;
    renderProducts();
  });

  document.getElementById("reset-filters").addEventListener("click", () => {
    state.brand = "";
    state.category = "";
    state.search = "";
    document.getElementById("search").value = "";
    document.getElementById("brand-filter").value = "";
    document.getElementById("category-filter").value = "";
    renderBrands();
    renderProducts();
  });
}

function init() {
  document.getElementById("year").textContent = new Date().getFullYear();
  applyTranslations();
  renderFilters();
  renderBrands();
  renderProducts();
  bindEvents();
}

document.addEventListener("DOMContentLoaded", init);
